#include "3-calc.h"

/**
 * op_add - adds integer parameters
 * @a: first parameter
 * @b: second parameter
 * Return: sum of parameters
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts integer parameters
 * @a: first parameter
 * @b: second parameter
 * Return: difference of parameters
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies integer parameters
 * @a: first parameter
 * @b: second parameter
 * Return: product of parameters
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides integer parameters
 * @a: first parameter
 * @b: second parameter
 * Return: quotient of parameters
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides integer parameters
 * @a: first parameter
 * @b: second parameter
 * Return: modulus of parameters
 */
int op_mod(int a, int b)
{
	return (a % b);
}
